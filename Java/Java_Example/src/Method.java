/*



접근 제어 (= Access Control)
접근 제어는 클래스의 멤버에 접근하는 것을 제어하는 방법이다.
public은 공용의 의미이며, 누구나 접근할 수 있다.
private은 전용의 의미이며, 클래스 안에서만 접근이 가능하다.
protected는 나중에 상속에서 설명한다.

일반적으로는 멤버변수인 필드에는 private을 사용하고 멤버함수인 메소드에는
public을 사용한다.

생성자
1. 생성자는 객체가 생성될 때 객체를 초기화하는 특수한 메소드이다.
2. 생성자의 이름은 클래스 이름과 같다
3. 생성자는 반환값을 가지지 않는다.
4. 생성자는 매개변수를 가지지 않을 수도 있고 가질 수도 있다.
생성자가 매개변수를 가지지 않는 경우에는 생성자 안에서 필드의 값을 지정한다.
생성자가 매개변수를 가지는 경우에는 전달된 매개변수의 값으로 필드의 값을 지정한다.

*/