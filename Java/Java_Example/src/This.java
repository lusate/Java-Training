/*
this는 객체 자기 자신을 나타낸다.
주로, 생성자의 매개변수 이름과 필드의 이름이 같은 경우에 필드 앞에
this를 사용하여 구별한다.

정적 변수 (Static Variable)
모든 객체를 통틀어서 하나만 생성하고 모든 객체들이 공유하는 변수를 정적 변수라고 한다

클래스의 필드를 정의할 때, 앞에다 static을 붙이면 정적 변수가 된다.
정적변수는 전역 변수와 같은 의미를 가진다.
정적변수를 사용하려면 클래스_이름 . 
클래스 이름 뒤에 멤버 연산자 .을 붙이고 정적 변수 이름을 쓴다

정적 메소드(Static Method)
메소드도 정적 메소드로 만들 수 있다.
메소드 선언 시 static을 메소드 앞에 붙이면 정적 메소드가 된다.
정적 메소드를 사용하려면 클래스_이름 . 메소드_이름 과 같이 클래스 이름 뒤에 멤버 연산자인
. 을 붙이고 정적 메소드 이름과 인수들을 쓴다.
*/